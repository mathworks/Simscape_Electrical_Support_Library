function g = igbtDV(x,y,lw,num)

g.m = plot([x x],[0 1]+y,...
           [x x]+0.1,[0 1]+y,...
           [x+0.1 x+0.4],[0.3 0.1]+y,...
           [x+0.1 x+0.4],[0.7 0.9]+y,...
           [x x-0.3],[0.5 0.5]+y,...
           [x+0.4 x+0.4],[0.9 1.2]+y,...
           [x+0.4 x+0.4],[-0.2 0.1]+y,...
           [x+0.25 x+0.4],[0.1 0.1]+y,...
           [x+0.32 x+0.4],[0.25 0.1]+y,...
           'LineWidth',lw,'Color','k');

g.d = plot([0.4 0.9]+x,[1.2 1.2]+y,...
           [0.4 0.9]+x,[-0.2 -0.2]+y,...
           [0.7 1.1]+x,[0.7 0.7]+y,...
           [0.7 1.1]+x,[0.3 0.3]+y,...
           [0.7 0.9]+x,[0.3 0.7]+y,...
           [1.1 0.9]+x,[0.3 0.7]+y,...
           [0.9 0.9]+x,[0.7 1.2]+y,...
           [0.9 0.9]+x,[0.3 -0.2]+y,...
           'LineWidth',lw,'Color','k');

g.td = text(x+1.2,y+0.5,['D',num],'Fontweight','bold','FontSize',12);
g.ts = text(x-0.9,y+0.5,['S',num],'Fontweight','bold','FontSize',12);

